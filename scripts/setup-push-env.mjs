// One-off setup: generate Web Push VAPID keys + a CRON_SECRET and append any
// that are missing to the local .env (gitignored). Idempotent — existing vars
// are left untouched. Secrets are written to disk only; nothing is printed.
//
//   node scripts/setup-push-env.mjs
//
// VAPID subject defaults to the email below; override with VAPID_SUBJECT_EMAIL.

import { readFileSync, existsSync, appendFileSync } from "node:fs";
import { randomBytes } from "node:crypto";
import webpush from "web-push";

const ENV_PATH = ".env";
const EXAMPLE_PATH = ".env.example";
const SUBJECT_EMAIL = process.env.VAPID_SUBJECT_EMAIL ?? "firdausnasir69@gmail.com";

const existing = existsSync(ENV_PATH) ? readFileSync(ENV_PATH, "utf8") : "";
const has = (key) => new RegExp(`^${key}=`, "m").test(existing);

const { publicKey, privateKey } = webpush.generateVAPIDKeys();

// Only fill what's absent so re-runs never rotate live keys.
const candidates = {
  VAPID_PUBLIC_KEY: publicKey,
  VAPID_PRIVATE_KEY: privateKey,
  NEXT_PUBLIC_VAPID_PUBLIC_KEY: publicKey,
  VAPID_SUBJECT: `mailto:${SUBJECT_EMAIL}`,
  CRON_SECRET: randomBytes(32).toString("hex"),
};

const missing = Object.entries(candidates).filter(([k]) => !has(k));

if (missing.length > 0) {
  const block =
    (existing && !existing.endsWith("\n") ? "\n" : "") +
    "\n# Web Push (PWA reminders) — generated by scripts/setup-push-env.mjs\n" +
    missing.map(([k, v]) => `${k}=${v}`).join("\n") +
    "\n";
  appendFileSync(ENV_PATH, block);
}

// Committed example carries names + safe placeholders only — never real values.
const examplePlaceholders = [
  "# Web Push (PWA reminders). Generate with: node scripts/setup-push-env.mjs",
  "VAPID_PUBLIC_KEY=",
  "VAPID_PRIVATE_KEY=",
  "NEXT_PUBLIC_VAPID_PUBLIC_KEY=",
  "VAPID_SUBJECT=mailto:you@example.com",
  "CRON_SECRET=",
];
const example = existsSync(EXAMPLE_PATH) ? readFileSync(EXAMPLE_PATH, "utf8") : "";
const exampleMissing = examplePlaceholders.filter(
  (line) => line.startsWith("#") || !new RegExp(`^${line.split("=")[0]}=`, "m").test(example),
);
if (exampleMissing.some((line) => !line.startsWith("#"))) {
  const block = (example && !example.endsWith("\n") ? "\n" : "") + "\n" + examplePlaceholders.join("\n") + "\n";
  appendFileSync(EXAMPLE_PATH, block);
}

// Report counts only — never the values (policy §7.1: no secrets in logs/context).
console.log(`.env: added ${missing.length} var(s) [${missing.map(([k]) => k).join(", ") || "none"}]`);
